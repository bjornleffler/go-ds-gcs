import (
	"math/rand"

	ds "github.com/ipfs/go-datastore"
)

var letters = []rune("ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789")

func randomSeq(n int) string {
	b := make([]rune, n)
	for i := range b {
		b[i] = letters[rand.Intn(len(letters))]
	}
	return string(b)
}

func randomKey() ds.Key {
	return ds.NewKey("/" + randomSeq(55))
}
